def RNAtoAminoAcids(RNA, geneticCode):
    pr = ""
    for k in range(len(RNA)):
        if((k + 1) % 3 == 0):
            am = geneticCode[RNA[k - 2:k + 1]]
            if(am == "*"):
                break
            pr += geneticCode[RNA[k - 2:k + 1]]
    return pr

